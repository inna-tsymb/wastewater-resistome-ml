import plotly.graph_objects as go

print("Генеруємо діаграму потоків Санкі...")

# 1. ВИЗНАЧАЄМО ВУЗЛИ (Nodes)
# Індекси: 0-Північна Америка, 1-Африка, 2-Південна Америка, 3-Океанія, 4-Близький Схід
# Індекси фінальних кошиків: 5-Європа (Низький AMR), 6-Азія (Високий AMR)
labels = [
    "Північна Америка",       # 0
    "Африка",                 # 1
    "Південна Америка",       # 2
    "Океанія",                # 3
    "Близький Схід",          # 4
    "Європейський профіль<br>(Низький AMR)", # 5
    "Азійський профіль<br>(Високий AMR)"     # 6
]

# Кольори для вузлів
node_colors = [
    "#2ecc71", # Північна Америка
    "#f1c40f", # Африка
    "#e67e22", # Південна Америка
    "#1abc9c", # Океанія
    "#9b59b6", # Близький Схід
    "#3498db", # Європейський профіль (Синій)
    "#e74c3c"  # Азійський профіль (Червоний)
]

# 2. ВИЗНАЧАЄМО ПОТОКИ (Links)
# Беремо результати з твоєї крос-континентальної валідації
sources = [0,  1,  1,  2,  2,  3,  4]  # Звідки йде потік (індекси)
targets = [5,  5,  6,  5,  6,  5,  5]  # Куди йде потік (індекси)
values =  [14, 2,  11, 4,  2,  3,  3]  # Кількість зразків

# Робимо кольори ліній напівпрозорими відповідно до кольору фінального кошика
# rgba(52, 152, 219, 0.4) - напівпрозорий синій, rgba(231, 76, 60, 0.4) - напівпрозорий червоний
link_colors = [
    "rgba(52, 152, 219, 0.4)", # NA -> EU
    "rgba(52, 152, 219, 0.4)", # AF -> EU
    "rgba(231, 76, 60, 0.4)",  # AF -> AS
    "rgba(52, 152, 219, 0.4)", # SA -> EU
    "rgba(231, 76, 60, 0.4)",  # SA -> AS
    "rgba(52, 152, 219, 0.4)", # OC -> EU
    "rgba(52, 152, 219, 0.4)"  # ME -> EU
]

# 3. СТВОРЮЄМО ОБ'ЄКТ ФІГУРИ
fig = go.Figure(data=[go.Sankey(
    node = dict(
      pad = 20,
      thickness = 30,
      line = dict(color = "black", width = 0.5),
      label = labels,
      color = node_colors
    ),
    link = dict(
      source = sources,
      target = targets,
      value = values,
      color = link_colors
    )
)])

# 4. НАЛАШТУВАННЯ ТА ЗБЕРЕЖЕННЯ
fig.update_layout(
    title_text="<b>Крос-континентальна валідація моделі резистому</b><br><span style='font-size:14px'>Розподіл зразків стічних вод за передбаченим рівнем AMR</span>",
    font_size=14,
    width=900,
    height=600
)

# Зберігаємо як HTML
fig.write_html("sankey_validation.html")

print("УСПІХ! Діаграму збережено у файл 'sankey_validation.html'.")